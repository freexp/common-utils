package com.jidian001.utils;

public class Bean {

    private String name;

    private int age;
    private char gender;

    private double assest;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public char getGender() {
        return gender;
    }

    public void setGender(char gender) {
        this.gender = gender;
    }

    public double getAssest() {
        return assest;
    }

    public void setAssest(double assest) {
        this.assest = assest;
    }
}
